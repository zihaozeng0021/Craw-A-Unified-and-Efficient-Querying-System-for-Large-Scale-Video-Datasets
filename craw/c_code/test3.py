from decimal import Decimal, getcontext


def format_float_with_two_decimals(value):
    # 设置精度为两位小数
    getcontext().prec = 10
    # 将浮点数转换为字符串，然后转换为 Decimal 类型
    decimal_value = Decimal(str(value))
    # 保留两位小数
    formatted_decimal = decimal_value.quantize(Decimal('0.00'))
    return formatted_decimal

def format_float(value1):
    getcontext().prec = 10
    decimal_value = Decimal(str(value1))
    format_float = decimal_value.quantize(Decimal('0.0'))
    return format_float

